<h1>Остров &laquo;Ольгин&raquo;</h1>

<p>Находится недалеко от Кончи-Заспы, примерно в 1,5 часа ходьбы на прогулочном теплоходе от ст.м. Днепр.</p>
<p>Растительность острова немного отличается от Великого &ndash; меньше соснового леса, большое количество кустарников, сильное речное течение вокруг острова. Это отличное место, чтобы убежать от цивилизации и отдохнуть в тесном кругу друзей или сотрудников.</p>  
