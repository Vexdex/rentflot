<br/>
<p>Нормальный состав учебной группы&nbsp;&mdash; это <nobr>4-5 человек,</nobr> что позволяет всем курсантам достаточно интенсивно участвовать в&nbsp;учебном процессе. Активность и&nbsp;настойчивость в&nbsp;изучении теории и&nbsp;практики и&nbsp;серьезный подход ко&nbsp;всему процессу обучения позволят Вам получить диплом INTERNATIONAL BAREBOAT CAPTAIN/SKIPPER (сертификат BSS) после двухнедельного зачетного плавания. Это даст Вам возможность самостоятельно брать яхты в&nbsp;аренду в&nbsp;любых яхт-чартерных компаниях и&nbsp;самостоятельно ходить на&nbsp;них по&nbsp;морям!</p>

