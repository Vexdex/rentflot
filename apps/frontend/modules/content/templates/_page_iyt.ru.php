<p>Обучение яхтингу проводят сертифицированные <strong>IYT</strong> опытные инструктора яхтенной школы <noindex><a href="http://www.yachting-rt.ru/school.html" rel="nofollow" target="_blank">Ветер (Россия)</a></noindex>, имеющие большой яхтенный стаж <nobr>(30-50 лет)</nobr> и&nbsp;соответствующий педагогический опыт. Для морской практики используются хорошие парусные круизные яхты Sun Odyssey&nbsp;44, Beneteau Oceanis 430, Beneteau Oceanis 320 и&nbsp;другие. 

