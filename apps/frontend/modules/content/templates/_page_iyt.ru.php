<p style="margin-bottom: 5px"><strong>Стоимость обучения:</strong></p>
  <ul>
    <li>обучение с&nbsp;проживанием (две недели) на&nbsp;яхте&nbsp;&mdash; <strong>1200 евро</strong> (включая сертификат <strong>BSS</strong>)</li>
    <li>обучение по&nbsp;программе VHF (радиооператор), 8&nbsp;часов&nbsp;&mdash; <strong>200 евро</strong> (включая сертификат <strong>VHF</strong>)</li>
    <li>паспорт яхтсмена, включая стоимость регистрационных марок&nbsp;&mdash; <strong>50&nbsp;евро</strong></li>
    <li>доплата за&nbsp;гарантированное одноместное размещение в&nbsp;двухместной каюте&nbsp;&mdash; <strong>100&nbsp;евро/неделя</strong></li>
  </ul>
</p>

<p><strong>Планируя свое время обучения, сверяйтесь с&nbsp;<noindex><a href="http://www.yachting-rt.ru/zagruzka.html" rel="nofollow" target="_blank">графиком и&nbsp;заполнением яхт учебного флота</a></noindex>.</strong></p>

<p style="margin-bottom: 5px"><strong>Скидки и&nbsp;бонусы:</strong></p>
  <ul>
    <li>бронирование учебы за&nbsp;шесть и&nbsp;более месяцев&nbsp;&mdash; <strong>50&nbsp;евро каждому курсанту</strong></li>
    <li><strong>групповая скидка: 100 евро</strong>&nbsp;&mdash; за&nbsp;двухнедельный курс для каждого дополнительного курсанта</li>
    <li>запись на&nbsp;учебу на&nbsp;выставке&nbsp;&mdash; <strong>100 евро</strong> за&nbsp;двухнедельный курс</li>
    <li>пассажирам (без обучения)&nbsp;&mdash; <strong>50&nbsp;евро/неделя</strong></li>
  </ul>
