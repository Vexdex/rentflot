<p style="margin-bottom: 5px"><strong>Стоимость аренды яхты зависит от&nbsp;следующего:</strong></p>
<ul>
  <li>вида (моторная, парусная);</li>
  <li>уровня (стандарт, люкс);</li>
  <li>года изготовления;</li>
  <li>даты начала аренды.</li>
</ul>

<p>Все яхты, которые мы&nbsp;предлагаем Вам, находятся в&nbsp;г. Шибеник (Хорватия), в&nbsp;яхт-клубе &laquo;Мандалина&raquo;, и&nbsp;это место выбрано не&nbsp;случайно. Шибеник расположен между двумя национальными парками, прекрасным архипелагом Корнати&nbsp;&mdash; самой протяженной группой островов, в&nbsp;которой почти 140 ненаселенных островов, и&nbsp;живописными водопадами реки Krka. Эта область журналом Nacional Geographic названа одним из&nbsp;самых привлекательных морских направлений в&nbsp;мире. Поскольку Шибеник находится в&nbsp;центре Адриатического побережья, то&nbsp;является идеальным стартом для путешествий на&nbsp;остров Вис, остров Хвар, остров Корчулу, а&nbsp;на&nbsp;юг&nbsp;побережья&nbsp;&mdash; в&nbsp;г. Дубровник, а&nbsp;так&nbsp;же в&nbsp;северную часть Адриатики&nbsp;&mdash; в&nbsp;Истрию или Бриюни.</p>










