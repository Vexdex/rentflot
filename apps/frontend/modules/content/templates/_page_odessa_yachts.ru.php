<p>В 120 км от Одессы располагается остров Змеиный (в древности известный как остров Левке) со скалистыми берегами от 4 до 25 метров. Это единственное вулкано-тектоническое поднятие на северо-западном шельфе Черного моря куда очень актуальны прогулки на яхте.</p>
<p>На острове:</p>
<ul>
