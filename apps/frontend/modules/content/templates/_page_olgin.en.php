<h1>«Olgin» island</h1>

<p>It locates near Koncha-Zaspa in about 1,5 hours away by walking from the Dnepr metro station.</p>
<p>The vegetation of the island is a bit different from “Velikiy” island: there is less pine forest, a lot of shrubs and strong river flow around the island. This is a great place to get away from civilization and relax in a close company of friends or employees.</p>
