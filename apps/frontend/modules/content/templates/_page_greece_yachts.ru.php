<p>Греция&nbsp;&mdash; страна мифов и&nbsp;морских путешественников! Яхтинг здесь полон невероятных открытий, с&nbsp;большой историей и&nbsp;традициями. Мы&nbsp;приглашаем Вас окунуться в&nbsp;этот прекрасный мир, обрести новые впечатления и&nbsp;посмотреть на&nbsp;мир с&nbsp;&laquo;другой стороны&raquo;.</p>
<p style="text-align: center; margin: 20px 0"><img src="/images/greece/2.jpg" alt="Яхт-клуб в Греции" title="Яхт-клуб в Греции" /></p>

<p>Наши партнеры находятся на&nbsp;нескольких островах Греции, и&nbsp;имеют не&nbsp;самый большой флот, но&nbsp;каждая предлагаемая яхта является их&nbsp;собственностью и&nbsp;поэтому находятся в&nbsp;отличном состоянии.</p>
<p>На&nbsp;острове Корфу (Corfu) мы&nbsp;сотрудничаем с&nbsp;мариной Gouvia, и&nbsp;прадлагаем Вам провереные яхты, основываясь на&nbsp;договоре непосредственно с&nbsp;яхтовладельцем, без дополнительных надбавок и&nbsp;накруток!</p>
<p style="text-align: center; margin: 20px 0"><img src="/images/greece/1.jpg" alt="Яхт-клуб в Греции" title="Яхт-клуб в Греции" /></p>

<p>Самый быстрый способ попасть на&nbsp;Корфу&nbsp;&mdash; по&nbsp;воздуху! Ежедневно из&nbsp;Афин и&nbsp;Салоник летают самолеты внутренних авиалиний Оlympic Airways и&nbsp;Aegean Airlines. Продолжительность перелета около 40&nbsp;мин.<br/>
Во&nbsp;время туристичского сезона, есь прямые чартерные перелеты из&nbsp;Киева.<br/>
От&nbsp;города Кофу до&nbsp;марины 6&nbsp;км (от&nbsp;Аэропорта 7&nbsp;км), т.е. такси будет стоить недорого.</p>
<p style="text-align: center; margin: 20px 0"><img src="/images/greece/3.jpg" alt="Яхт-клуб в Греции" title="Яхт-клуб в Греции" /></p>

<p style="margin-bottom: 5px"><strong>Стоимость аренды яхты зависит от&nbsp;следующего:</strong></p>
<ul>
  <li>вида (моторная, парусная);</li>
  <li>уровня (стандарт, люкс);</li>
  <li>года изготовления;</li>
  <li>даты начала аренды.</li>
</ul>

