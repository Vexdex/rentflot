<h1>«Velikiy» island</h1>

<p>The island is located at Vyshgorod (near Kiev reservoir) in about 1,5 hours away by walking from the metro station Dnepr</p>
<p>This is the most beautiful place in the city with magnificent beaches, clean sand and a variety of meadows, pine forests, occupying almost the entire western half of the island. That's what delights at Velikiy island.</p>
